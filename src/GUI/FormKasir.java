    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String valKODEPRODUK  = edKodeProduk.getText();
        String valJENISPRODUK = cbJenisProduk.getSelectedItem().toString();
        String valNAMAPRODUK  = edNamaProduk.getText();
        String valHARGAPRODUK = edHargaProduk.getText();
        
        Connection objCon = new KoneksiDB().getConnection();
        try{
            String query_insert = "insert into produk VALUES (?,?,?,?)";
            PreparedStatement statement = objCon.prepareStatement(query_insert);
                statement.setString(1, valKODEPRODUK);
                statement.setString(2, valJENISPRODUK);
                statement.setString(3, valNAMAPRODUK);
                statement.setString(4, valHARGAPRODUK);
               
                statement.executeUpdate();
                statement.close();
            
        } catch (SQLException ex){
                JOptionPane.showMessageDialog(null,"Gagal memasukkan data");
                Logger.getLogger(FormKasir.class.getName()).log(Level.SEVERE, null, ex);
                } 
        
        tampil();
    }//GEN-LAST:event_btnCreateActionPerformed

