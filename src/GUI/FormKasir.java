    public void tampil(){
        KoneksiDB classKoneksiDB = new KoneksiDB();
        try{
            con = classKoneksiDB.getConnection();
            st = con.createStatement();
            rs = st.executeQuery("Select * FROM produk");
            while(rs.next ()){
                Object[] obj = new Object[5];
                obj[0] = rs.getString("KODE_PRODUK");
                obj[1] = rs.getString("JENIS_PRODUK");
                obj[2] = rs.getString("NAMA_PRODUK");
                obj[3] = rs.getString("HARGA_PRODUK");
                model.addRow(obj);
            }
            jtbKasir.setModel(model);
            st.close();
            
            }catch(SQLException err){
                Logger.getLogger(FormKasir.class.getName()).log(Level.SEVERE, null, err);
                JOptionPane.showMessageDialog(null, err.getMessage() );
            }
    }
    
