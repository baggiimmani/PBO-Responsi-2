    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            Statement st = con.createStatement();
            String search = edSearch.getText();
            ResultSet rs = st.executeQuery("SELECT * FROM produk WHERE KODE_PRODUK like '%"+search+"%' OR NAMA_PRODUK like '%"+search+"%'");
            DefaultTableModel dtm = (DefaultTableModel) jtbKasir.getModel();

            dtm.setRowCount(0);
            String [] data = new String[6];
            int i = 1;

            while(rs.next())
            {
                data[0] = rs.getString("KODE_PRODUK");
                data[1] = rs.getString("JENIS_PRODUK");
                data[2] = rs.getString("NAMA_PRODUK");
                data[3] = rs.getString("HARGA_PRODUK");
                dtm.addRow(data);
                i++;
            }
            rs.close();
            edSearch.setText("");
        }
        catch(Exception ex)  {
            JOptionPane.showMessageDialog(null,"Data yang Anda cari Tidak dapat Ditemukan");
            System.err.println("error (search) : " +ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

